    <h3 id="相对关系">相对关系 </h3>
    <p>刘徽对数学概念的定义抽象而严谨。他揭示了概念的本质，基本符合现代<a
        href="https://baike.baidu.com/item/%E9%80%BB%E8%BE%91%E5%AD%A6/85559?fromModule=lemma_inlink">逻辑学</a>和数学对概念定义的要求。而且他使用概念时亦保持了其<a
        href="https://baike.baidu.com/item/%E5%90%8C%E4%B8%80%E6%80%A7/7549351?fromModule=lemma_inlink">同一性</a>。如他提出凡数相与者谓之率，把率定义为数量的相互关系。又如他把正负数定义为今两算得失相反，要令正负以名之，摆脱了正为余，负为欠的原始观念，从本质上揭示了正负数得失相反的相对关系。
    </p>
    <p>《九章算术》的算法尽管抽象，但相互关系不明显，显得零乱。刘徽大大发展深化了中算中久已使用的率概念和齐同原理，把它们看作运算的纲纪。许多问题，只要找出其中的各种率关系，通过乘以散之，约以聚之，齐同以通之，都可以归结为今有术求解。
    </p>
    <p>一平面（或立体）图形经过平移或旋转，其面积（或体积）不变。把一个平面（或立体）图形分解成若干部分，各部分面积（或体积）之和与原<a
        href="https://baike.baidu.com/item/%E5%9B%BE%E5%BD%A2%E9%9D%A2%E7%A7%AF/1864539?fromModule=lemma_inlink">图形面积</a>（或体积）相等。基于这两条不言自明的前提的出入相补原理，是中国<a
        href="https://baike.baidu.com/item/%E5%8F%A4%E4%BB%A3%E6%95%B0%E5%AD%A6/0?fromModule=lemma_inlink">古代数学</a>进行几何推演和证明时最常用的原理。刘徽发展了<a
        href="https://baike.baidu.com/item/%E5%87%BA%E5%85%A5%E7%9B%B8%E8%A1%A5%E5%8E%9F%E7%90%86/2362417?fromModule=lemma_inlink">出入相补原理</a>，成功地证明了许多面积、体积以及可以化为面积、体积问题的勾股、开方的公式和算法的正确性。
    </p>
    <h3 id="逻辑定义">逻辑定义 </h3>
    <p>刘徽对《九章算术》中的所有数学概念都做了解释或逻辑定义，在解释和定义中，他非常注意数学推理的逻辑性，充分考虑各问题之间的逻辑关系.
      在“勾股”章的注释中，明确指出：这一章之所以一开头就提出了勾股定理，是因为“将以施于诸率，故先具此术，以见其源也”. 刘徽用这一精彩的论述，从“逻辑”角度注释了勾股定理出现在“勾股”章开头的必要性.
      刘徽认为有些问题不能只限于感性认识，必须在感性认识的基础上提升到理性认识的层面，并在理性认识的基础上形成数学理论. 因而，他从逻辑严谨性出发，对于那些能从逻辑上证明的法则都进行了论证。</p>
    <h2 id="数学成就">数学成就 </h2>
    <h3 id="算术">算术 </h3>
    <p>
      （1）、在算术方面的主要成就有分数运算、比例问题和“盈不足”算法。《九章算术》是世界上最早系统叙述了分数运算的著作，在第二、三、六章中有许多比例问题，在世界上也是比较早的。“盈不足”的算法需要给出两次假设，是一项创造，中世纪欧洲称它为“<a
        href="https://baike.baidu.com/item/%E5%8F%8C%E8%AE%BE%E6%B3%95/15679605?fromModule=lemma_inlink">双设法</a>”，有人认为它是由中国经中世纪<a
        href="https://baike.baidu.com/item/%E9%98%BF%E6%8B%89%E4%BC%AF%E5%9B%BD%E5%AE%B6/328200?fromModule=lemma_inlink">阿拉伯国家</a>传去的。
    </p>
    <p>《九章算术》中有比较完整的分数计算方法，包括<a
        href="https://baike.baidu.com/item/%E5%9B%9B%E5%88%99%E8%BF%90%E7%AE%97/5337481?fromModule=lemma_inlink">四则运算</a>，<a
        href="https://baike.baidu.com/item/%E9%80%9A%E5%88%86/1613474?fromModule=lemma_inlink">通分</a>、约分、化<a
        href="https://baike.baidu.com/item/%E5%B8%A6%E5%88%86%E6%95%B0/3441263?fromModule=lemma_inlink">带分数</a>为<a
        href="https://baike.baidu.com/item/%E5%81%87%E5%88%86%E6%95%B0/2666302?fromModule=lemma_inlink">假分数</a>（中国古代称为<a
        href="https://baike.baidu.com/item/%E9%80%9A%E5%88%86%E5%86%85%E5%AD%90/19137560?fromModule=lemma_inlink">通分内子</a>，“内”读为纳）等等。其步骤与方法大体与现代的雷同。
    </p>
    <p>分数加减运算，《九章算术》已明确提出先通分，使两分数的分母相同，然后进行加减。加法的步骤是“母互乘子，并以为实，母相乘为法，实如法而一”这里“实”是分子。“法”是<a
        href="https://baike.baidu.com/item/%E5%88%86%E6%AF%8D/5421449?fromModule=lemma_inlink">分母</a>，“实如法而一”也就是用法去除实，进行除法运算，《九章算术》还注意到两点：其一是运算结果如出现“不满法者，以法命之”。就是分子小于分母时便以分数形式保留。其二是“其母同者，直相从之”，就是分母相同的分数进行加减，运算时不必<a
        href="https://baike.baidu.com/item/%E9%80%9A%E5%88%86/0?fromModule=lemma_inlink">通分</a>，使分子直接加减即可。</p>
    <p>《九章算术》中还有求最大公约数和约分的方法。求<a
        href="https://baike.baidu.com/item/%E6%9C%80%E5%A4%A7%E5%85%AC%E7%BA%A6%E6%95%B0/869308?fromModule=lemma_inlink">最大公约数</a>的方法称为“更相减损”法，其具体步骤是“可半者半之，不可半者，副置分母子之数，以少减多，更相减损，求其等也。以等数约之。”这里所说的“等数”就是最大公约数。可半者是指分子分母都是偶数，可以折半的先把它们折半，即可先约去2。不都是偶数了，则另外摆（即副置）分子<a
        href="https://baike.baidu.com/item/%E5%88%86%E6%AF%8D/5421449?fromModule=lemma_inlink">分母</a>算筹进行计算，从大数中减去小数，<a
        href="https://baike.baidu.com/item/%E8%BE%97%E8%BD%AC%E7%9B%B8%E5%87%8F/7529647?fromModule=lemma_inlink">辗转相减</a>，减到<a
        href="https://baike.baidu.com/item/%E4%BD%99%E6%95%B0/6180737?fromModule=lemma_inlink">余数</a>和<a
        href="https://baike.baidu.com/item/%E5%87%8F%E6%95%B0/8525331?fromModule=lemma_inlink">减数</a>相等，即得等数。</p>
    <p>在《九章算术》的第二、三、六等章内，广泛地使用了各种比例解应用问题。<a
        href="https://baike.baidu.com/item/%E7%B2%9F%E7%B1%B3/1830255?fromModule=lemma_inlink">粟米</a>章的开始就列举了各种粮食间互换的比率如下：“粟米之法：粟率五十，粝米三十，粺米二十七，糳米二十四，……”这是说：谷子五斗去皮可得<a
        href="https://baike.baidu.com/item/%E7%B3%99%E7%B1%B3/6125543?fromModule=lemma_inlink">糙米</a>三斗，又可舂得九折米二斗七升，或八拆米二斗四升，……。例如，粟米章第一题：“今有粟米一斗，欲为粝米，问得几何”。它的解法是：“以所有数乘所求率为实，以所有率为法，实如法而一”。
    </p>
    <p>《九章算术》第七章“盈不足”专讲<a
        href="https://baike.baidu.com/item/%E7%9B%88%E4%BA%8F%E9%97%AE%E9%A2%98/287024?fromModule=lemma_inlink">盈亏问题</a>及其解法其中第一题：“今有（人）共买物，（每）人出八（钱），盈（余）三钱；人出七（钱），不足四（钱），问人数、物价各几何”，“答曰：七人，物价53（钱）。”“<a
        href="https://baike.baidu.com/item/%E7%9B%88%E4%B8%8D%E8%B6%B3%E6%9C%AF/4181048?fromModule=lemma_inlink">盈不足术</a>曰：置所出率，盈、不足各居其下。令维乘（即交错相乘）所出率，并以为实，并盈，不足为法，实如法而一……置所出率，以少减多，余，以约法、实。实为物价，法为人数”。盈不足术是<a
        href="https://baike.baidu.com/item/%E4%B8%AD%E5%9B%BD%E6%95%B0%E5%AD%A6%E5%8F%B2/8791485?fromModule=lemma_inlink">中国数学史</a>上解应用问题的一种别开生面的创造，它在中国古代算法中占有相当重要的地位。盈不足术还经过<a
        href="https://baike.baidu.com/item/%E4%B8%9D%E7%BB%B8%E4%B9%8B%E8%B7%AF/434?fromModule=lemma_inlink">丝绸之路</a>西传<a
        href="https://baike.baidu.com/item/%E4%B8%AD%E4%BA%9A/553445?fromModule=lemma_inlink">中亚</a>阿拉伯国家，受到特别重视，被称为“契丹算法”，后来又传入欧洲，中世纪时期“双设法”曾长期统治了他们的数学王国。
    </p>
    <h3 id="几何">几何 </h3>
    <p>《九章算术》总结了生产、生活实践中大量的几何知识，在方田、<a
        href="https://baike.baidu.com/item/%E5%95%86%E5%8A%9F/10549828?fromModule=lemma_inlink">商功</a>和勾股章中提出了很多面积、体积的<a
        href="https://baike.baidu.com/item/%E8%AE%A1%E7%AE%97%E5%85%AC%E5%BC%8F/19277353?fromModule=lemma_inlink">计算公式</a>和勾股定理的应用。
    </p>
    <p>《九章算术》方田章主要论述平面图形<a
        href="https://baike.baidu.com/item/%E7%9B%B4%E7%BA%BF%E5%BD%A2/18891529?fromModule=lemma_inlink">直线形</a>和圆的面积计算方法。《九章算术》方田章第一题“今有田广十五步，从（音纵zong）十六步。问为田几何。”“答曰：一亩”。这里“广”就是宽，“从”即纵，指其长度，“方田术曰：广从步数相乘得积步，（得积步就是得到乘积的平方步数）以亩法二百四十步（实质应为积步）除之，即亩数。百亩为一顷。”当时称长方形为方田或直田。称<a
        href="https://baike.baidu.com/item/%E4%B8%89%E8%A7%92%E5%BD%A2/179552?fromModule=lemma_inlink">三角形</a>为圭田，<a
        href="https://baike.baidu.com/item/%E9%9D%A2%E7%A7%AF%E5%85%AC%E5%BC%8F/3696832?fromModule=lemma_inlink">面积公式</a>为“术曰：半广以乘正从”。这里广是指三角形的<a
        href="https://baike.baidu.com/item/%E5%BA%95%E8%BE%B9/755766?fromModule=lemma_inlink">底边</a>，正从是指底边上的高，刘徽在注文中对这一计算公式实质上作了证明：“半广者，以盈补虚，为直田也。”“亦可以半正从以乘广”（图1－30）。盈是多余，虚乃不足。“以盈补虚”就是以多余部分填补不足的部分，这就是中国<a
        href="https://baike.baidu.com/item/%E5%8F%A4%E4%BB%A3%E6%95%B0%E5%AD%A6/3955260?fromModule=lemma_inlink">古代数学</a>推导<a
        href="https://baike.baidu.com/item/%E5%B9%B3%E9%9D%A2%E5%9B%BE%E5%BD%A2/2005359?fromModule=lemma_inlink">平面图形</a><a
        href="https://baike.baidu.com/item/%E9%9D%A2%E7%A7%AF%E5%85%AC%E5%BC%8F/3696832?fromModule=lemma_inlink">面积公式</a>所用的传统的“出入相补”的方法，由上图“以盈补虚”变圭田为与之等积的直田，于是得到了圭田的面积计算公式。
    </p>
    <p>方田章第二十七、二十八题把<a
        href="https://baike.baidu.com/item/%E7%9B%B4%E8%A7%92%E6%A2%AF%E5%BD%A2/4447740?fromModule=lemma_inlink">直角梯形</a>称为“<a
        href="https://baike.baidu.com/item/%E9%82%AA%E7%94%B0/19137567?fromModule=lemma_inlink">邪田</a>”（即斜田）它的面积公式是：“术曰：并两邪（即两斜，应理解为梯形两底）而半之，以乘正从……，又可半正从……以乘并。”刘徽在注中说明他的证法仍是“出入相补”法。在方田章第二十九、三十题把一般梯形称为“<a
        href="https://baike.baidu.com/item/%E7%AE%95%E7%94%B0/19137575?fromModule=lemma_inlink">箕田</a>”，上、下底分别称为“舌”、“踵”，面积公式是：“术曰：并踵舌而半之，以乘正从”。
    </p>
    <p>至于圆面积，在《九章算术》方田章第三十一、三十二题中，它的面积计算公式为：“半周半径相乘得积步”。这里“周”是<a
        href="https://baike.baidu.com/item/%E5%9C%86%E5%91%A8%E9%95%BF/2631209?fromModule=lemma_inlink">圆周长</a>，“径”是指直径。这个<a
        href="https://baike.baidu.com/item/%E5%9C%86%E9%9D%A2%E7%A7%AF/2360908?fromModule=lemma_inlink">圆面积</a>计算公式是正确的。只是当时取径一周三（即π≈3）。于是由此计算所得的圆面积就不够精密。
    </p>
    <p>《九章算术》商功章收集的都是一些有关体积计算的问题。但是商功章并没有论述<a
        href="https://baike.baidu.com/item/%E9%95%BF%E6%96%B9%E4%BD%93/697638?fromModule=lemma_inlink">长方体</a>或<a
        href="https://baike.baidu.com/item/%E6%AD%A3%E6%96%B9%E4%BD%93/2541160?fromModule=lemma_inlink">正方体</a>的体积算法。看来《九章算术》是在长方体或正方体体积计算公式：V=abc的基础上来计算其他<a
        href="https://baike.baidu.com/item/%E7%AB%8B%E4%BD%93%E5%9B%BE%E5%BD%A2/3183362?fromModule=lemma_inlink">立体图形</a>体积的。
    </p>
    <p>《九章算术》商功章提到城、垣、堤、沟、堑、渠，因其功用不同因而名称各异，其实质都是正截面为<a
        href="https://baike.baidu.com/item/%E7%AD%89%E8%85%B0%E6%A2%AF%E5%BD%A2/10895226?fromModule=lemma_inlink">等腰梯形</a>的<a
        href="https://baike.baidu.com/item/%E7%9B%B4%E6%A3%B1%E6%9F%B1/2172487?fromModule=lemma_inlink">直棱柱</a>，他们的体积计算方法：“术曰：并上、下广而半之，以高若深乘之，又以袤乘之，即积尺”。这里上、下广指<a
        href="https://baike.baidu.com/item/%E6%A8%AA%E6%88%AA%E9%9D%A2/3338627?fromModule=lemma_inlink">横截面</a>的上、下底（a，b）高或深（h），袤是指城垣……的长（l）。因此城、垣…的体积计算术公式V=1/2（a+b）h.
    </p>
    <p>刘徽在注释中把对于平面图形的出入相补原理推广应用到<a
        href="https://baike.baidu.com/item/%E7%A9%BA%E9%97%B4%E5%9B%BE%E5%BD%A2/1968629?fromModule=lemma_inlink">空间图形</a>，成为“损广补狭”以证明<a
        href="https://baike.baidu.com/item/%E5%87%A0%E4%BD%95%E4%BD%93/8492899?fromModule=lemma_inlink">几何体</a><a
        href="https://baike.baidu.com/item/%E4%BD%93%E7%A7%AF%E5%85%AC%E5%BC%8F/8880018?fromModule=lemma_inlink">体积公式</a>。
    </p>
    <p>刘徽还用棋验法来推导比较复杂的几何体体积计算公式。所谓棋验法，“棋”是指某些几何体模型即用几何体<a
        href="https://baike.baidu.com/item/%E6%A8%A1%E5%9E%8B%E9%AA%8C%E8%AF%81/22514999?fromModule=lemma_inlink">模型验证</a>的方法，例如<a
        href="https://baike.baidu.com/item/%E9%95%BF%E6%96%B9%E4%BD%93/0?fromModule=lemma_inlink">长方体</a>本身就是“棋”[图1－32（1）]斜解一个长方体，得两个两底面为<a
        href="https://baike.baidu.com/item/%E7%9B%B4%E8%A7%92%E4%B8%89%E8%A7%92%E5%BD%A2/234388?fromModule=lemma_inlink">直角三角形</a>的<a
        href="https://baike.baidu.com/item/%E7%9B%B4%E4%B8%89%E6%A3%B1%E6%9F%B1/218599?fromModule=lemma_inlink">直三棱柱</a>，中国古代称为“<a
        href="https://baike.baidu.com/item/%E5%A0%91%E5%A0%B5/5591186?fromModule=lemma_inlink">堑堵</a>”（如图1），所以堑堵的体积是长方体体积的二分之一。
    </p>
    <p>《九章算术》商功章还有圆锥、<a
        href="https://baike.baidu.com/item/%E5%9C%86%E5%8F%B0/10076861?fromModule=lemma_inlink">圆台</a>（古代称“圆亭”）的体积计算公式。甚至对三个侧面是<a
        href="https://baike.baidu.com/item/%E7%AD%89%E8%85%B0%E6%A2%AF%E5%BD%A2/0?fromModule=lemma_inlink">等腰梯形</a>，其他两面为<a
        href="https://baike.baidu.com/item/%E5%8B%BE%E8%82%A1%E5%BD%A2/4504718?fromModule=lemma_inlink">勾股形</a>的<a
        href="https://baike.baidu.com/item/%E4%BA%94%E9%9D%A2%E4%BD%93/8328276?fromModule=lemma_inlink">五面体</a>[图1－33（1）]，上、下底为矩形的拟
    </p>
    <p><a href="https://baike.baidu.com/item/%E6%9F%B1%E4%BD%93/198102?fromModule=lemma_inlink">柱体</a>（古代称“<a
        href="https://baike.baidu.com/item/%E5%88%8D%E7%AB%A5/7262771?fromModule=lemma_inlink">刍童</a>”）以及上底为一线段，下底为一矩形的<a
        href="https://baike.baidu.com/item/%E6%8B%9F%E6%9F%B1%E4%BD%93/2126117?fromModule=lemma_inlink">拟柱体</a>（古代称“刍甍”）（“甍”音“梦”）等都可以计算其体积。
    </p>
    <h3 id="代数">代数 </h3>
    <p>《九章算术》中的代数内容同样很丰富，具有当时世界的先进水平。</p>
    <p>1．开平方和开立方</p>
    <p>
      《九章算术》中讲了开平方、开立方的方法，而且计算步骤基本一样。所不同的是古代用筹算进行演算，现以少广章第12题为例，说明古代开平方演算的步骤，“今有积五万五千二百二十五步。问为方几何”。“答曰：二百三十五步”。这里所说的步是中国古代的<a
        href="https://baike.baidu.com/item/%E9%95%BF%E5%BA%A6%E5%8D%95%E4%BD%8D/9535255?fromModule=lemma_inlink">长度单位</a>。
    </p>
    <p>“开方（是指开平方，由<a
        href="https://baike.baidu.com/item/%E6%AD%A3%E6%96%B9%E5%BD%A2/1019418?fromModule=lemma_inlink">正方形</a>面积求其一边之长。）术曰：置积为实（即指<a
        href="https://baike.baidu.com/item/%E7%AD%B9%E7%AE%97/24205447?fromModule=lemma_inlink">筹算</a>中把<a
        href="https://baike.baidu.com/item/%E8%A2%AB%E5%BC%80%E6%96%B9%E6%95%B0/1613605?fromModule=lemma_inlink">被开方数</a>放置于第二行，称为实）借一算（指借用一<a
        href="https://baike.baidu.com/item/%E7%AE%97%E7%AD%B9/954669?fromModule=lemma_inlink">算筹</a>放置于最后一行，用以定位）。步之（指所借的算筹一步一步移动）超一等（指所借的算筹由<a
        href="https://baike.baidu.com/item/%E4%B8%AA%E4%BD%8D/2415092?fromModule=lemma_inlink">个位</a>越过十位移至百位或由百位越过千位移至万位等等，这与现代笔算开平方中分节相当）。议所得（指议得初商，由于实的万位数字是5，而且2²＜5＜3²，议得初商为2，而借算在万位，因此应在第一行置初商2于百位）。以一乘所借一算为法（指以初商2乘所借算一次为20000，置于“实”下为“法”）而以除（指以初商2乘“法”20000得40000，由“实”减去得：55225-40000=15225）除已，倍法为定法，其复除，折法而下（指将“法”加倍，向右移一位，得4000为“定法”因为要求<a
        href="https://baike.baidu.com/item/%E5%B9%B3%E6%96%B9%E6%A0%B9/79171?fromModule=lemma_inlink">平方根</a>的十位数字，需要把“借算”移至百位）。复置借算步之如初，以复议一乘之，所得副，以加定法，以除（这一段是指：要求平方根的十位数字，需置借算于百位。因“实”的千位数字为15，且4×3＜15＜4×4，于是再议得次商为3。置3于商的十位。以次商3乘借算得3×100=300，与定法相加为4000+300=4300。再乘以次商，则得：3×4300=12900，由“实”减去得：15225－12900=2325。以所得副从定法，复除折下如前：这一段是指演算如前，即再以300×1+4300=4600向右移一位，得460，是第三位<a
        href="https://baike.baidu.com/item/%E6%96%B9%E6%A0%B9/7806355?fromModule=lemma_inlink">方根</a>的定法，再把借算移到个位；又议得三商应为5，再置5于商的个位，以5+460=465，再乘以三商5，得465×5=2325经计算恰尽，因此得<a
        href="https://baike.baidu.com/item/%E5%B9%B3%E6%96%B9%E6%A0%B9/79171?fromModule=lemma_inlink">平方根</a>为235。）</p>
    <p>上述由图1－25（1）—（10）是按算筹进行演算的，看起来似乎很繁琐，实际上步骤十分清楚，易于操作。它的开平方原理与现代开平方原理相同。其中“借算”的右移、<a
        href="https://baike.baidu.com/item/%E5%B7%A6%E7%A7%BB/56641765?fromModule=lemma_inlink">左移</a>在现代的观点下可以理解为一次变换和代换。《九章算术》时代并没有理解到变换和代换，但是这对以后宋、元时期<a
        href="https://baike.baidu.com/item/%E9%AB%98%E6%AC%A1%E6%96%B9%E7%A8%8B/5000427?fromModule=lemma_inlink">高次方程</a>的解法是有深远影响的。
    </p>
    <p>《九章算术》方程章中的“方程”是专指多元一次<a
        href="https://baike.baidu.com/item/%E6%96%B9%E7%A8%8B%E7%BB%84/5695032?fromModule=lemma_inlink">方程组</a>而言，与“方程”的含义并不相同。《九章算术》中多元一次方程组的解法，是将它们的系数和<a
        href="https://baike.baidu.com/item/%E5%B8%B8%E6%95%B0%E9%A1%B9/1531686?fromModule=lemma_inlink">常数项</a>用<a
        href="https://baike.baidu.com/item/%E7%AE%97%E7%AD%B9/0?fromModule=lemma_inlink">算筹</a>摆成“方阵”（所以称之谓“方程”）。<a
        href="https://baike.baidu.com/item/%E6%B6%88%E5%85%83/14743414?fromModule=lemma_inlink">消元</a>的过程相当于现代大学课程<a
        href="https://baike.baidu.com/item/%E9%AB%98%E7%AD%89%E4%BB%A3%E6%95%B0/344763?fromModule=lemma_inlink">高等代数</a>中的<a
        href="https://baike.baidu.com/item/%E7%BA%BF%E6%80%A7%E5%8F%98%E6%8D%A2/5904192?fromModule=lemma_inlink">线性变换</a>。
    </p>
    <p>由于《九章算术》在用<a
        href="https://baike.baidu.com/item/%E7%9B%B4%E9%99%A4%E6%B3%95/10768920?fromModule=lemma_inlink">直除法</a>解一次方程组过程中，不可避免地要出现正负数的问题，于是在方程章第三题中明确提出了正负术。刘徽在该术的注文里实质上给出了正、负数的定义：“两算得失相反，要令‘正’、‘负’以名之”。并在<a
        href="https://baike.baidu.com/item/%E8%AE%A1%E7%AE%97%E5%B7%A5%E5%85%B7/447520?fromModule=lemma_inlink">计算工具</a>即算筹上加以区别“正算赤，负算黑，否则以邪正为异”。这就是规定<a
        href="https://baike.baidu.com/item/%E6%AD%A3%E6%95%B0/1294288?fromModule=lemma_inlink">正数</a>用红色算筹，负数用黑色算筹。如果只有同色算筹的话，则遇到正数将筹正放，负数<a
        href="https://baike.baidu.com/item/%E6%97%B6%E9%82%AA/2742107?fromModule=lemma_inlink">时邪</a>（同斜）放。宋代以后出现笔算也相应地用红、黑色数码字以区别正、负数，或在<a
        href="https://baike.baidu.com/item/%E4%B8%AA%E4%BD%8D%E6%95%B0/1602759?fromModule=lemma_inlink">个位数</a>上记斜划以表示负数，如（即—1824），后来这种包括负数写法在内的中国数码字还传到日本。
    </p>
    <p>关于正、负数的加减<a
        href="https://baike.baidu.com/item/%E8%BF%90%E7%AE%97%E6%B3%95%E5%88%99/230422?fromModule=lemma_inlink">运算法则</a>，“正负术曰：同名相益，异名相除，正无入负之，负无入正之。其异名相除，同名相益，正无入正之，负无入负之”。这里所说的“同名”、“异名”分别相当于所说的同号、异号。“相益”、“相除”是指二数相加、相减。术文前四句是减法运算法则：
    </p>
    <p>（1）如果<a href="https://baike.baidu.com/item/%E8%A2%AB%E5%87%8F%E6%95%B0/8471713?fromModule=lemma_inlink">被减数</a><a
        href="https://baike.baidu.com/item/%E7%BB%9D%E5%AF%B9%E5%80%BC/3995097?fromModule=lemma_inlink">绝对值</a>大于减数绝对值，即a＞b≥0，
    </p>
    <p>则同名相益：（±a）-（±b）=±（a-b），</p>
    <p>异名相除：（±a）-（b）=±（a+b）。</p>
    <p>（2）如果被减数绝对值小于减数绝对值，即b＞a≥0。</p>
    <p>①如果两数皆正</p>
    <p>则a-b=a-[a+（b-a）]=-（b-a）。</p>
    <p>中间一式的a和a对消，而（b-a）无可对消，则改“正”为“负”，即“正无入负之”。“无入”就是无对，也就是无可对消（或不够减或对方为零）。</p>
    <p>②如果两数皆负</p>
    <p>则（-a）-（-b）=－a-[（-a）-（b-a）]=+（b-a）。在中间的式子里（-a）和（-a）对消，而-（b-a）无可对消，则改“负”为“正”所以说“负无入正之”。</p>
    <p>③如果两数一正一负。则仍同（1）的异名相益。</p>
    <p>术文的后四句是指正负数加法运算法则。</p>
    <p>（1）同号两数相加，即同名相益，其和的绝对值等于两数绝对值和。</p>
    <p>如果a＞0，b＞0，</p>
    <p>则a+b=a+b，（-a）+（-b）=-（a+b）</p>
    <p>（2）异号两数相加，实为相减，即异名相除。如果<a
        href="https://baike.baidu.com/item/%E6%AD%A3%E6%95%B0/0?fromModule=lemma_inlink">正数</a>的绝对值较大，其和为正，即“正无入正之”。如果负数的绝对值较大，其和为负，即“负无入负之”。用符号表示为
    </p>
    <p>①如果a＞b≥0，</p>
    <p>则 a+（-b）=[b+（a-b）]+（-b）=a-b，</p>
    <p>或 （-a）+b=[（-b）－（a-b）]+b=-（a-b）。</p>
    <p>②如果b＞a≥0，</p>
    <p>则 a+（-b）=a+[（-a）-（b-a）]=-（b-a），</p>
    <p>或 （-a）+b=（-a）+[a+（b-a）]=b-a。</p>
    <p>关于正负数的乘除法则，在《九章算术》时代或许会遇到有关正负数的乘除运算。可惜书中并未论及，直到元代<a
        href="https://baike.baidu.com/item/%E6%9C%B1%E4%B8%96%E6%9D%B0/363534?fromModule=lemma_inlink">朱世杰</a>于《<a
        href="https://baike.baidu.com/item/%E7%AE%97%E5%AD%A6%E5%90%AF%E8%92%99/5121928?fromModule=lemma_inlink">算学启蒙</a>》（1299年）中才有明确的记载：“同名相乘为正，异名相乘为负”，“同名相除所得为正，异名相除所得为负”，因此至迟于13世纪末中国对<a
        href="https://baike.baidu.com/item/%E6%9C%89%E7%90%86%E6%95%B0/105546?fromModule=lemma_inlink">有理数</a>四则运算法则已经全面作了总结。至于正负<a
        href="https://baike.baidu.com/item/%E6%95%B0%E6%A6%82%E5%BF%B5/3240759?fromModule=lemma_inlink">数概念</a>的引入，正负数加减运算法则的形成的<a
        href="https://baike.baidu.com/item/%E5%8E%86%E5%8F%B2%E8%AE%B0%E5%BD%95/9985545?fromModule=lemma_inlink">历史记录</a>，中国更是遥遥领先。国外首先承认负数的是七世纪印度数学家婆罗门岌多（约598-？）欧洲到16世纪才承认负数。
    </p>
    <h2 id="后世影响">后世影响 </h2>
    <h3 id="古代影响">古代影响 </h3>
    <p>《九章算术》是世界上最早系统叙述了分数运算的著作；其中盈不足的算法更是一项令人惊奇的创造；“方程”章还在世界<a
        href="https://baike.baidu.com/item/%E6%95%B0%E5%AD%A6%E5%8F%B2/5310815?fromModule=lemma_inlink">数学史</a>上首次阐述了负数及其加减运算法则。在代数方面，《九章算术》在世界数学史上最早提出负数概念及正负数加减法法则；中学讲授的<a
        href="https://baike.baidu.com/item/%E7%BA%BF%E6%80%A7%E6%96%B9%E7%A8%8B%E7%BB%84/5904308?fromModule=lemma_inlink">线性方程组</a>的解法和《九章算术》介绍的方法大体相同。注重实际应用是《九章算术》的一个显着特点。该书的一些知识还传播至印度和<a
        href="https://baike.baidu.com/item/%E9%98%BF%E6%8B%89%E4%BC%AF/1736939?fromModule=lemma_inlink">阿拉伯</a>，甚至经过这些地区远至欧洲。
    </p>
    <p>
      《九章算术》是几代人共同劳动的结晶，它的出现标志着中国古代数学体系的形成．后世的数学家，大都是从《九章算术》开始学习和研究数学知识的。唐宋两代都由国家明令规定为教科书。1084年由当时的北宋朝廷进行刊刻，这是世界上最早的印刷本数学书。可以说，《九章算术》是中国为数学发展做出的又一杰出贡献。
    </p>
    <p>在九章算术中有许多数学问题都是世界上记载最早的。例如，关于比例算法的问题，它和后来在16世纪<a
        href="https://baike.baidu.com/item/%E8%A5%BF%E6%AC%A7/3028649?fromModule=lemma_inlink">西欧</a>出现的三分律的算法一样。关于双设法的问题，在阿拉伯曾称为契丹算法，13世纪以后的欧洲数学著作中也有如此称呼的，这也是中国<a
        href="https://baike.baidu.com/item/%E5%8F%A4%E4%BB%A3%E6%95%B0%E5%AD%A6/0?fromModule=lemma_inlink">古代数学</a>知识向西方传播的一个证据。
    </p>
    <p>
      《九章算术》对中国古代的数学发展有很大影响，这种影响一直持续到了清朝中叶。《九章算术》的叙述方式以归纳为主，先给出若干例题，再给出解法，不同于西方以演绎为主的叙述方式，中国后来的数学著作也都是采用叙述方式为主。历代数学家有不少人曾经注释过这本书，其中以<a
        href="https://baike.baidu.com/item/%E5%88%98%E5%BE%BD/42748?fromModule=lemma_inlink">刘徽</a>和<a
        href="https://baike.baidu.com/item/%E6%9D%8E%E6%B7%B3%E9%A3%8E/1963353?fromModule=lemma_inlink">李淳风</a>的注释最有名。
    </p>
    <p>《九章算术》还流传到了<a
        href="https://baike.baidu.com/item/%E6%97%A5%E6%9C%AC/111617?fromModule=lemma_inlink">日本</a>和朝鲜，对其古代的数学发展也产生了很大的影响。
    </p>
    <p>作为一部世界数学名著，《九章算术》早在<a
        href="https://baike.baidu.com/item/%E9%9A%8B%E5%94%90/8550815?fromModule=lemma_inlink">隋唐</a>时期即已传入朝鲜、日本。它已被译成日、俄、德、法等多种文字版本。
    </p>
